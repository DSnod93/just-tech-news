// POST MODEL
// DEPENDENCIES
// SEQUELIZE MODEL, DATATYPES, AND DATABASE CONNECTION
const { Model, DataTypes } = require('sequelize');
const sequelize = require('../config/connection');

// THE POST MODEL EXTENDS THE SEQUELIZE MODEL 
class Post extends Model {
    // MODEL METHOD FOR THE UPVOTE FUNCTION
    // THIS CODE WAS PREVIOUSLY IN THE API/POSTS/UPVOTE ROUTE
    static upvote(body, models) {
        return models.Vote.create({
            user_id: body.user_id,
            post_id: body.post_id
        }).then(() => {
            return Post.findOne({
                where: {
                    id: body.post_id
                },
                attributes: [
                    'id',
                    'post_url',
                    'title',
                    'created_at',
                    [
                        sequelize.literal('(SELECT COUNT(*) FROM vote WHERE post.id = vote.post_id)'),
                        'vote_count'
                    ]
                ]
            });
        });
    }
}

// DEFINE THE TABLE COLUMNS AND CONFIGURATION, SIMILAR TO THE SETUP FOR THE USER MODEL
Post.init(
    {
        id: {
            type: DataTypes.INTEGER,
            allowNull: false,
            primaryKey: true,
            autoIncrement: true
        },
        title: {
            type: DataTypes.STRING,
            allowNull: false
        },
        post_url: {
            type: DataTypes.STRING,
            allowNull: false,
            validate: {
                isURL: true
            }
        },
        user_id: {
            type: DataTypes.INTEGER,
            references: {
                model: 'user',
                key: 'id'
            }
        }
    },
    {
        sequelize,
        freezeTableName: true,
        underscored: true,
        modelName: 'post'
    }
)

// EXPORT THE MODEL
module.exports = Post;
